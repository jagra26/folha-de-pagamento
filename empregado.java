package com.company;

public class empregado {
    String nome;
    String endereco;
    double salario;
    boolean sindicato;
    int tipo;
    int horas;
    double taxa;
    

    public void setNome(string nome)
    {
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getEndereco() {
        return endereco;
    }
}
